<template>
  <p class="control" :class="{ 'full-width': fullWidth }">
    <NeoButton
      class="explore-tabs-button"
      :tag="tag"
      :no-shadow="noShadow"
      :active="active"
      :to="to"
      :icon="icon">
      <span> {{ text }}</span>
      <span v-if="count" class="ml-2 has-text-grey">{{
        formatLargeNumber(count)
      }}</span>
    </NeoButton>
  </p>
</template>

<script setup lang="ts">
import { NeoButton } from '@kodadot1/brick'
import { formatLargeNumber } from '@/utils/format/number'

const props = withDefaults(
  defineProps<{
    active: boolean
    text: string
    count?: number
    to?: string
    fullWidth?: boolean
    noShadow?: boolean
    tag?: string
    showActiveCheck?: boolean
  }>(),
  {
    to: '',
    tag: 'nuxt-link',
    showActiveCheck: true,
  }
)

const icon = computed(() =>
  props.showActiveCheck && props.active ? 'check' : ''
)
</script>

